int get_font_index(char a)
{
    switch (a)
        {
            case ' ' : return 26;
            case '!' : return 27;
            case '.' : return 28;
            case '?' : return 29;
            default:
                if(a >= 'a' && a <= 'z')
                    return (a-'a');
                else if(a >= 'A' && a <= 'Z')
                    return (a-'A');
                else
                    return MAX_FONT_INDEX-1; //return the empty character if it cant be found so we fail gracefully
        }
         
}

